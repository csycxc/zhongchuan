package com.csicerc.dss.domain;
/**
 * 网站分类实体类
 * @author Administrator
 *
 */
public class WebCategory {
	
	private Integer webCategoryId;
	private String webCategoryName;
	private Integer webCategoryStatus;
	public Integer getWebCategoryId() {
		return webCategoryId;
	}
	public void setWebCategoryId(Integer webCategoryId) {
		this.webCategoryId = webCategoryId;
	}
	public String getWebCategoryName() {
		return webCategoryName;
	}
	public void setWebCategoryName(String webCategoryName) {
		this.webCategoryName = webCategoryName;
	}
	public Integer getWebCategoryStatus() {
		return webCategoryStatus;
	}
	public void setWebCategoryStatus(Integer webCategoryStatus) {
		this.webCategoryStatus = webCategoryStatus;
	}
	
	
	
	
	
	
}
